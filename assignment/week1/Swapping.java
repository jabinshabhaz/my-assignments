package assignment.week1;

public class Swapping {
	public static void main(String[] args) {
		//declare a variable need to swap
		int a=100;
		int b=50;
		//add 2 values and assign it to a
		a=a+b;
		//assign the subtracted values of a and b to b
		b=a-b;
		//assign the subtracted values of a and b to a
		a=a-b;
		//print a and b
		System.out.println("swapped value of a= "+a);
		System.out.println("swapped value of b= "+b);
		
		
	}

}
